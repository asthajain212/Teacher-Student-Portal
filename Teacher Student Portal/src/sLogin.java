    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        
        
        String _id,_password;
        if (!id.getText().equals("") && !password.getText().equals("")) {
            
            MyDatabaseHandler db = new MyDatabaseHandler();
            db.connectDatabase();
            
            _id = id.getText();
            _password = password.getText();
            
            ResultSet rs = db.testQuerySt();
            int res = db.checkResultSt(rs,_id,_password);
            
            if(res==1){
                
                Course c = new Course();
                c.setVisible(true);
                c.getId(_id);
                dispose();
            }
        }
    }//GEN-LAST:event_loginActionPerformed

